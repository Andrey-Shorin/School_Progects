int	ft_strlcpy(char *a, const char *b, int n)
{
	int	i;

	i = 0;
	while (i != n - 1 && b[i] != 0 && n != 0)
	{
		a[i] = b[i];
		i++;
	}
	if (n != 0)
		a[i] = 0;
	while (b[i] != 0)
	{
		i++;
	}
	return (i);
}
