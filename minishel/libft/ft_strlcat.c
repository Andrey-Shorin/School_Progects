int	ft_strlcat(char *a, const char *b, int n)
{
	int	i;
	int	g;
	int	v;

	i = 0;
	g = 0;
	while (a[g] != 0 && (g <= n - 1 || n == -1))
		g++;
	v = g;
	while (((i != n - 1 && g < n - 1) || n == -1) && b[i] != 0 )
	{
		a[g] = b[i];
		i++;
		g++;
	}
	if (i != 0)
		a[g] = 0;
	while (b[i] != 0)
	{
		i++;
	}
	return (i + v);
}
